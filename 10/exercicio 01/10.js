function clicar() {
    var a = window.document.getElementById("area");
    a.textContent = "Clicou!";
    a.style.background = 'red'
  }
  
  function entrar() {
    var a = window.document.getElementById("area");
    a.textContent = "Entrou!";
    a.style.background = 'green'
  }
  
  function sair() {
    var a = window.document.getElementById("area");
    a.textContent = "Saiu!";
    a.style.background = 'red'
  }
  